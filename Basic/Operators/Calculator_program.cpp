#include<iostream>
using namespace std;
int main(){
    int a,b;
    cout<<"Enter a: "<<endl;
    cin>>a;

    cout<<"Enter b: "<<endl;
    cin>>b;

    cout<<"Addition is: "<<a+b<<endl;
    cout<<"Subtraction is: "<<a-b<<endl;
    cout<<"Multiplication is: "<<a*b<<endl;
    cout<<"Division is: "<<a/b<<endl;
    cout<<"Modulo is: "<<a%b<<endl;
    
    return 0;
}